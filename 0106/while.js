/**
 *  while(조건문) {
 *  반복할 코드
 * }
 * 주의 사항: 조건문에 탈출 조건이 있어야하거나
 * 혹은 조건문에 탈출 조건이 없으면 break를 넣어야함
 */

//step1.

//step2.

//step3. 1~10까지 홀수 출력
let step3 = 10;
while (step3 <= 1) {
    if (step3 % 2 == 1) {
        console.log(step3);
    }
    step3++;
}

//step4.  break사용
let step4 = 0;
while (true) {
    console.log(step4);
    step4++;
    if (step4 === 10) {
        break;
    }
}

